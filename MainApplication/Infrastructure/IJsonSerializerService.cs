namespace MainApplication.Infrastructure
{
    /* ---------------------------------------------------------
     * インターフェース定義(JSONシリアライズサービス)
     * --------------------------------------------------------- */

    public interface IJsonSerializerService
    {
        /* -----------------------------------------------------
         * シリアライズ(オブジェクト → JSON)
         * ----------------------------------------------------- */

        string Serialize<T>(T obj);

        /* -----------------------------------------------------
         * デシリアライズ(JSON → オブジェクト)
         * ----------------------------------------------------- */

        T? Deserialize<T>(string json);
    }
}

